/*键盘输入一维正整数数组a[10]，计算数组中的所有元素被第一个元素除的结果，要求仍然保存在原来的数组中。例如：原数组为{2，3，4},执行函数后数组为{1，1，2}。*/
#include<stdio.h>
int main() {
	int a[10];
	int i, t;
	//输入数组
	for (i = 0; i <= 9; i++)
	{
		scanf("%d", &a[i]);
	}
	t = a[0];
	//转变
	for (i = 0; i <= 9; i++)
	{
		a[i] = a[i] / t;
	}
     //输出
	for (i = 0; i <= 9; i++)
	{
		printf("%d ",a[i]);
	}
	return 0;
}
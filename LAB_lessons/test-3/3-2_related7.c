/*统计并输出2023以内与7相关的所有正整数个数
说明：与7相关的含义：该数是7的倍数或该数中包含7*/
#include <stdio.h>

int main() {
    int count = 0;
    int i ;

    // 统计并输出2023以内与7相关的所有正整数的个数
    for (i = 1; i <= 2023; i++)
    {
        // 判断该数是7的倍数或该数中包含7
        if (i % 7 == 0 || i % 10 == 7)
        {
            count++;
        }
    }

    printf("2023以内与7相关的正整数个数为：%d\n", count);

    return 0;
}

